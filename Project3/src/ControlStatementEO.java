
public class ControlStatementEO {
   public static void findEvenOrOdd(int num) {
	   if(num%2==0) {
		   System.out.println(num+" is even");
	   }else {
		   System.out.println(num+" is odd");
	   }
   }
    public static void main(String[]args) {
    	findEvenOrOdd(44);
    	findEvenOrOdd(23);
    	findEvenOrOdd(98);
 } 
}   
